 */
class User extends Authenticatable
{
    use HasUuids, HasApiTokens, HasFactory, Notifiable;

    protected $guarded = [];

    protected $casts = [
      'country' => CountryNames::class,
    ];

    protected $hidden = [
      'password', 'remember_token'
    ];

    public function posts(): HasMany
    {
        return $this->hasMany(Post::class);
    }
}
